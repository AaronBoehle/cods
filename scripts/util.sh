#################################
# A handful of helper functions #
#################################

heading(){
    echo '---------------------------------------------------------------------'
    echo "> $@"
    echo '---------------------------------------------------------------------'
}

confirm() {
	local message=${1:-'Continue?'}
	read -p "${message} [y/N] " input
	[[ ${input,,} = y* ]]
}

die() {
    echo "$@"
    exit 1
}

